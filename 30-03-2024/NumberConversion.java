//write a program to assign binary, octal and hexadecimal values to an integer variable and print the values

public class NumberConversion {
    public static void main(String[] args) {
        int i = 0b111;
        System.out.println("The binary value of i is " + i);
        int j = 05;
        System.out.println("The octal value of j is " + j);
        int k = 0xF;
        System.out.println("The hexadecimal value of k is " + k);
    }
}
